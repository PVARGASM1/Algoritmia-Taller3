// Una temperatura Celsius (centígrados) puede ser convertida a una temperatura equivalente 
// Fahrenheit de acuerdo con la siguiente fórmula:
// F=(9/5)C + 32
// Codifique en JavaScript un algoritmo que lea la temperatura en grados Celsius y 
// la escriba en Fahrenheit.

let celsius = 30

f = (9/5)*celsius + 32;

console.log ("La temperatura en Fahrenheit es: " + f);